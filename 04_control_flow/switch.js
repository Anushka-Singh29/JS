const month = 3

switch (month) {
    case 1:
        console.log("january")
        break;
    case 2:
        console.log("feb")
        break;
    case 3:
        console.log("march")
    //    break;
    case 4:
        console.log("april")
        break;
    case 5:
        console.log("may")
        break;
    case 6:
        console.log("june")
        break;
    case 7:
        console.log("july")
        break;
    case 8:
        console.log("august")
        break;
    case 9:
        console.log("sept")
        break;
    case 10:
        console.log("oct")
        break;

    default:
        console.log("Default case matched")
        break;
}

